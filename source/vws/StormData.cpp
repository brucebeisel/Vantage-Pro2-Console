#include "StormData.h"

namespace vws {

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
StormData::StormData() : stormState(StormState::STORM_IDLE), stormRain(0) {
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
StormData::StormData(const DateTimeFields & start, const DateTimeFields & end, Rainfall rain) : stormState(StormState::STORM_IDLE),
                                                                                                stormStart(start),
                                                                                                stormEnd(end),
                                                                                                stormRain(rain) {
    if (stormStart.isDateTimeValid() && !stormEnd.isDateTimeValid() && stormRain > 0.0)
        stormState = StormState::STORM_ACTIVE;
    else if (stormStart.isDateTimeValid() && stormEnd.isDateTimeValid() && stormRain > 0.0)
        stormState = StormState::STORM_ENDED;

}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
StormData::~StormData() {
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void
StormData::resetStormData() {
    stormStart.resetDateTimeFields();
    stormEnd.resetDateTimeFields();
    stormState = StormState::STORM_IDLE;
    stormRain = 0.0;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
bool
StormData::setStormStart(const DateTimeFields & start, Rainfall rain) {
    if (!start.isDateTimeValid())
        return false;

    stormStart.setDateTime(start);
    stormRain = rain;

    return true;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void
StormData::setStormStart(int year, int month, int day, Rainfall rain) {
    stormStart.setDate(year, month, day);
    stormRain = rain;
    stormState = StormState::STORM_ACTIVE;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
bool
StormData::setStormEnd(const DateTimeFields & end) {
    if (!end.isDateTimeValid())
        return false;

    stormEnd.setDateTime(end);
    stormState = StormState::STORM_ENDED;
    return true;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void
StormData::setStormEnd(int year, int month, int day) {
    stormEnd.setDate(year, month, day);
    stormState = StormState::STORM_ENDED;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void
StormData::setStormRain(Rainfall rainfall) {
    stormRain = rainfall;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
bool
StormData::setStormData(const DateTimeFields & start, const DateTimeFields & end, Rainfall rain) {
    if (!start.isDateTimeValid() || !end.isDateTimeValid())
        return false;

    setStormStart(start, rain);
    setStormEnd(end);
    return true;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
bool
StormData::isStormActive() const {
    return stormState == StormState::STORM_ACTIVE;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
bool
StormData::hasStormEnded() const {
    return stormState == StormState::STORM_ENDED;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
const DateTimeFields &
StormData::getStormStart() const {
    return stormStart;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
const DateTimeFields &
StormData::getStormEnd() const {
    return stormEnd;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
Rainfall
StormData::getStormRain() const {
    return stormRain;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
bool
StormData::operator<(const StormData & other) const {
    return stormStart < other.stormStart;
}
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
std::ostream &
operator<<(std::ostream & os, const StormData & sd)  {
    os << "Storm: Start: " << sd.stormStart.formatDate() << " End: " << sd.stormEnd.formatDate() << " Rainfall: " << sd.stormRain;
    return os;
}
}
